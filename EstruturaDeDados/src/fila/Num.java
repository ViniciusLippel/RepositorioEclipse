package fila;

public class Num {
	
	private int indice;
	private int num;
	private Num proximo;
	
	public int getIndice() {
		return indice;
	}
	public void setIndice(int indice) {
		this.indice = indice;
	}
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	public Num getProximo() {
		return proximo;
	}
	public void setProximo(Num proximo) {
		this.proximo = proximo;
	}
	
}

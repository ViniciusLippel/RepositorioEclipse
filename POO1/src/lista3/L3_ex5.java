
package lista3;

public class L3_ex5 {
    public static void main(String[] args) {
        final int n = 20;
        
        for (int i=1; i<=n; i++){
            System.out.println(i);
        }
        
        System.out.println("\n");
        
        for (int i=1; i<=n; i++){
            System.out.print(i+"; ");
        }
    }
}
